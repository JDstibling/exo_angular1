export class Video {

    

    constructor(public image: String, public title: String, public views: Number, public uploaderName: String, public duration: Number, public language: String){
        this.image = image;
        this.title = title;
        this.views = views;
        this.uploaderName = uploaderName;
        this.duration = duration;
        this.language = language;
    }
    

}


  
